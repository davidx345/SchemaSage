@dataclass
class IntegrationCredentials:
    """Credentials for integration authentication"""
    auth_type: AuthenticationType
    credentials: Dict[str, Any]
    expires_at: Optional[datetime] = None
    refresh_token: Optional[str] = None
    
    def is_expired(self) -> bool:
        """Check if credentials are expired"""
        if self.expires_at is None:
            return False
        return datetime.now() >= self.expires_at
    
    def needs_refresh(self, buffer_minutes: int = 5) -> bool:
        """Check if credentials need refresh within buffer time"""
        if self.expires_at is None:
            return False
        buffer_time = datetime.now() + timedelta(minutes=buffer_minutes)
        return buffer_time >= self.expires_at

