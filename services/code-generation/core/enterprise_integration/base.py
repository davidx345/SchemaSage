class BaseIntegration(ABC):
    """Base class for all integrations"""
    
    def __init__(self, config: IntegrationConfig):
        self.config = config
        self.client = None
        self.is_connected = False
        self.metrics = ConnectionMetrics()
    
    @abstractmethod
    async def connect(self) -> bool:
        """Establish connection to the integration"""
        pass
    
    @abstractmethod
    async def disconnect(self):
        """Close connection to the integration"""
        pass
    
    @abstractmethod
    async def test_connection(self) -> Dict[str, Any]:
        """Test the integration connection"""
        pass
    
    @abstractmethod
    async def get_schema(self) -> Optional[SchemaResponse]:
        """Get schema information from the integration"""
        pass
    
    async def refresh_credentials(self) -> bool:
        """Refresh authentication credentials if needed"""
        return True
    
    async def get_health_status(self) -> IntegrationHealth:
        """Get health status of the integration"""
        try:
            test_result = await self.test_connection()
            is_healthy = test_result.get("success", False)
            status_message = test_result.get("message", "Unknown status")
            
            return IntegrationHealth(
                integration_id=self.config.integration_id,
                is_healthy=is_healthy,
                status_message=status_message,
                last_check=datetime.now(),
                metrics=self.metrics
            )
        except Exception as e:
            return IntegrationHealth(
                integration_id=self.config.integration_id,
                is_healthy=False,
                status_message=f"Health check failed: {str(e)}",
                last_check=datetime.now(),
                metrics=self.metrics,
                issues=[str(e)]
            )
    
    def update_metrics(self, success: bool, response_time: float):
        """Update connection metrics"""
        self.metrics.total_requests += 1
        self.metrics.last_request_time = datetime.now()
        
        if success:
            self.metrics.successful_requests += 1
        else:
            self.metrics.failed_requests += 1
        
        # Update average response time
        total_time = self.metrics.average_response_time * (self.metrics.total_requests - 1)
        self.metrics.average_response_time = (total_time + response_time) / self.metrics.total_requests
        
        # Update error rate
        self.metrics.error_rate = (self.metrics.failed_requests / self.metrics.total_requests) * 100

def validate_integration_config(config: IntegrationConfig) -> List[str]:
    """Validate integration configuration"""
    issues = []
    
    if not config.integration_id:
        issues.append("Integration ID is required")
    
    if not config.name:
        issues.append("Integration name is required")
    
    if not config.endpoint_url:
        issues.append("Endpoint URL is required")
    
    if not config.credentials:
        issues.append("Credentials are required")
    
    # Validate URL format
    try:
        from urllib.parse import urlparse
        parsed = urlparse(config.endpoint_url)
        if not parsed.scheme or not parsed.netloc:
            issues.append("Invalid endpoint URL format")
    except:
        issues.append("Invalid endpoint URL")
    
    # Validate credentials based on auth type
    if config.credentials:
        auth_type = config.credentials.auth_type
        creds = config.credentials.credentials
        
        if auth_type == AuthenticationType.API_KEY and not creds.get("api_key"):
            issues.append("API key is required for API key authentication")
        
        elif auth_type == AuthenticationType.BASIC_AUTH:
            if not creds.get("username") or not creds.get("password"):
                issues.append("Username and password are required for basic authentication")
        
        elif auth_type == AuthenticationType.OAUTH2:
            required_fields = ["client_id", "client_secret"]
            for field in required_fields:
                if not creds.get(field):
                    issues.append(f"{field} is required for OAuth2 authentication")
    
    return issues

